//C While Loop
#include <stdio.h>
#include <stdlib.h>

/*
--> Loops can execute a block of code as long as a specified condition is reached.

while (condition) {
  // code block to be executed
}

*/


int main()
{
	int i = 0;

	while (i < 5) {
  	printf("%d\n", i);
  	i++;

}

    return 0;
}
