#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    /*
     HOW to Use <math.h> library.
    ceil floor sqrt pow abs
    */
    printf("%f\n",ceil(4.3));	 // 5.000000
    printf("%f\n",floor(4.9));	 // 4.000000
    printf("%f\n",sqrt(67)); 	 // 8.185353
    printf("%f\n",pow(3,3));	 // 27.000000
    printf("%d\n",abs(15)); 	 //15

    return 0;
    
}
